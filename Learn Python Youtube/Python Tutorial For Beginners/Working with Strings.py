phrase = "Giraffe Academy"
print(phrase.lower())  # make entire phrase lowercase
print(phrase.isupper())  # boolean for if phrase is uppercase
print(phrase.upper().isupper())
print(len(phrase))  # display length of phrase
print(phrase[0])  # grab the first character in phrase
print(phrase.index("G"))  # return the index of the "G" in phrase
print(phrase.replace("Giraffe", "Elephant"))  # replace "Giraffe" with "Elephant"
