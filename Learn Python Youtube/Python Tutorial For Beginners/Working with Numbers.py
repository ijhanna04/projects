from math import *  # import module "math" that enables more math functions (lines 19-21)

print(2)
print(3 + 4.5 * 5)  # follows order of operations ( * then + )
print(3 * (4 + 5))  # follows order of operations
print(10 % 3)  # displays the remainder of 10 / 3
print(10 / 3)  # displays decimal answers for math done with integers unlike C++

my_num = 5
print(my_num)
print(str(my_num) + " my favorite number")  # convert number to string

print(abs(-6))  # absolute value of -6
print(pow(3, 2))  # 3 to the power of 2
print(max(4, 6))  # print the number with the highest value
print(min(4, 6))  # print the number with the lowest value
print(round(3.2))  # round a number to the nearest integer

print(floor(3.7))  # round a number down
print(ceil(3.2))  # round a number up
print(sqrt(36))  # square root of 36
